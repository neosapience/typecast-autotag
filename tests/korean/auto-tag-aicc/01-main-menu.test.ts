import { autoTag } from '../../../src/korean/auto-tag';

/**
 * AICC 시나리오 01: 메인 메뉴 안내
 *
 * ARS 시스템의 메인 메뉴 안내 시나리오입니다.
 * 고객이 전화를 걸면 처음 듣는 메인 메뉴 안내 멘트를 테스트합니다.
 */
describe('AICC 시나리오 01: 메인 메뉴 안내', () => {
  it('메인 메뉴 안내 멘트를 올바르게 변환한다', () => {
    const input = `안녕하세요, 타입캐스트 고객센터입니다.
상담 품질 향상을 위해 통화 내용이 녹음됩니다.

메뉴 안내입니다.
요금 및 결제 문의는 1번,
배송 조회는 2번,
주문 취소 및 환불은 3번,
회원 정보 변경은 4번,
포인트 및 쿠폰 조회는 5번,
이벤트 및 프로모션 안내는 6번,
기타 문의는 7번,
상담원 연결은 0번을 눌러주세요.

현재 상담 대기 시간은 약 5m30s입니다.
상담원 연결 없이 셀프 서비스를 원하시면 8번을 눌러주세요.

고객센터 운영시간은 09:00부터 18:00까지이며,
점심시간은 12:00부터 13:00입니다.

다시 듣기를 원하시면 별표를 눌러주세요.`;

    const expected = `안녕하세요, 타입캐스트 고객센터입니다.
상담 품질 향상을 위해 통화 내용이 녹음됩니다.

메뉴 안내입니다.
요금 및 결제 문의는 일번,
배송 조회는 이번,
주문 취소 및 환불은 삼번,
회원 정보 변경은 사번,
포인트 및 쿠폰 조회는 오번,
이벤트 및 프로모션 안내는 육번,
기타 문의는 칠번,
상담원 연결은 영번 을 눌러주세요.

현재 상담 대기 시간은 약 오분 삼십초 입니다.
상담원 연결 없이 셀프 서비스를 원하시면 팔번 을 눌러주세요.

고객센터 운영시간은 오전 아홉시 부터 오후 여섯시 까지이며,
점심시간은 오후 열두시 부터 오후 한시 입니다.

다시 듣기를 원하시면 별표를 눌러주세요.`;

    expect(autoTag(input)).toBe(expected);
  });

  it('긴급 안내 멘트를 올바르게 변환한다', () => {
    const input = `안녕하세요, 타입캐스트입니다.
현재 시스템 점검 중으로 2024-01-15T22:00부터 2024-01-16T06:00까지
일부 서비스 이용이 제한될 수 있습니다.

긴급 문의는 1588-1234로 연락 주시기 바랍니다.
불편을 드려 죄송합니다.`;

    const expected = `안녕하세요, 타입캐스트입니다.
현재 시스템 점검 중으로 이천이십사년 일월 십오일 오후 열시 부터 이천이십사년 일월 십육일 오전 여섯시 까지
일부 서비스 이용이 제한될 수 있습니다.

긴급 문의는 일 . 오 . 팔 . 팔 . 일 . 이 . 삼 . 사 로 연락 주시기 바랍니다.
불편을 드려 죄송합니다.`;

    expect(autoTag(input)).toBe(expected);
  });

  it('휴일 안내 멘트를 올바르게 변환한다', () => {
    const input = `안녕하세요, 타입캐스트 고객센터입니다.
죄송합니다. 현재 영업시간이 아닙니다.

고객센터 운영시간은 평일 09:00부터 18:00까지이며,
토요일, 일요일 및 공휴일은 휴무입니다.

2024년 설날 연휴 기간 안내드립니다.
2월9일부터 2월12일까지 휴무이며,
2월13일부터 정상 운영됩니다.

긴급 문의는 홈페이지 1:1 문의를 이용해 주세요.
감사합니다.`;

    const expected = `안녕하세요, 타입캐스트 고객센터입니다.
죄송합니다. 현재 영업시간이 아닙니다.

고객센터 운영시간은 평일 오전 아홉시 부터 오후 여섯시 까지이며,
토요일, 일요일 및 공휴일은 휴무입니다.

이천이십사년 설날 연휴 기간 안내드립니다.
이월 구일 부터 이월 십이일 까지 휴무이며,
이월 십삼일 부터 정상 운영됩니다.

긴급 문의는 홈페이지 일대일 문의를 이용해 주세요.
감사합니다.`;

    expect(autoTag(input)).toBe(expected);
  });
});
