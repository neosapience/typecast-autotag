import { autoTag } from '../../../src/korean/auto-tag';

/**
 * AICC 시나리오 02: 요금 및 결제 문의
 *
 * 고객이 요금 및 결제 관련 문의를 할 때의 ARS 안내 시나리오입니다.
 */
describe('AICC 시나리오 02: 요금 및 결제 문의', () => {
  it('결제 금액 안내 멘트를 올바르게 변환한다', () => {
    const input = `요금 및 결제 안내입니다.

고객님의 이번 달 청구 금액은 59,900원입니다.
납부 기한은 2024-01-25까지입니다.

결제 내역 상세 안내입니다.
기본요금 39,900원,
부가서비스 15,000원,
할인금액 마이너스 5,000원,
부가세 포함 총 59,900원입니다.

자동이체 등록을 원하시면 1번,
카드 결제를 원하시면 2번,
결제 내역 SMS 발송을 원하시면 3번,
이전 메뉴로 돌아가시려면 별표를 눌러주세요.`;

    const expected = `요금 및 결제 안내입니다.

고객님의 이번 달 청구 금액은 오만구천구백 원 입니다.
납부 기한은 이천이십사년 일 월 이십오 일 까지입니다.

결제 내역 상세 안내입니다.
기본요금 삼만구천구백 원,
부가서비스 일만오천 원,
할인금액 마이너스 오천 원,
부가세 포함 총 오만구천구백 원 입니다.

자동이체 등록을 원하시면 일 번,
카드 결제를 원하시면 이 번,
결제 내역 SMS 발송을 원하시면 삼 번,
이전 메뉴로 돌아가시려면 별표를 눌러주세요.`;

    expect(autoTag(input)).toBe(expected);
  });

  it('미납 요금 안내 멘트를 올바르게 변환한다', () => {
    const input = `미납 요금 안내입니다.

고객님께서는 현재 2건의 미납 내역이 있습니다.

1번째 미납: 2023년12월 청구분 45,000원
납부 기한: 2023-12-25 (30일 연체)

2번째 미납: 2024년1월 청구분 52,000원
납부 기한: 2024-01-25 (5일 연체)

총 미납 금액은 97,000원입니다.
연체료 2,500원이 추가되어 총 99,500원을 납부하셔야 합니다.

즉시 납부를 원하시면 1번,
분할 납부 상담을 원하시면 2번,
상담원 연결을 원하시면 0번을 눌러주세요.`;

    const expected = `미납 요금 안내입니다.

고객님께서는 현재 두 건 의 미납 내역이 있습니다.

첫 번째 미납: 이천이십삼년 십이 월 청구분 사만오천 원
납부 기한: 이천이십삼년 십이 월 이십오 일 (삼십일 연체)

두 번째 미납: 이천이십사년 일 월 청구분 오만이천 원
납부 기한: 이천이십사년 일 월 이십오 일 (오일 연체)

총 미납 금액은 구만칠천 원 입니다.
연체료 이천오백 원 이 추가되어 총 구만구천오백 원 을 납부하셔야 합니다.

즉시 납부를 원하시면 일 번,
분할 납부 상담을 원하시면 이 번,
상담원 연결을 원하시면 영 번 을 눌러주세요.`;

    expect(autoTag(input)).toBe(expected);
  });

  it('결제 완료 안내 멘트를 올바르게 변환한다', () => {
    const input = `결제가 완료되었습니다.

결제 정보 안내입니다.
결제 금액: 125,000원
결제 수단: 신용카드
결제 일시: 2024-01-15T14:32
승인 번호: 12345678

영수증 발송을 원하시면 1번,
메인 메뉴로 돌아가시려면 별표를 눌러주세요.

이용해 주셔서 감사합니다.`;

    const expected = `결제가 완료되었습니다.

결제 정보 안내입니다.
결제 금액: 십이만오천 원
결제 수단: 신용카드
결제 일시: 이천이십사년 일 월 십오 . 일 . 오후 두 시 삼십이 분
승인 번호: 일 . 이 . 삼 . 사 . 오 . 육 . 칠 . 팔 . 영수증 발송을 원하시면 일 번,
메인 메뉴로 돌아가시려면 별표를 눌러주세요.

이용해 주셔서 감사합니다.`;

    expect(autoTag(input)).toBe(expected);
  });
});
