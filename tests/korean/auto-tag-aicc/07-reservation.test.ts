import { autoTag } from '../../../src/korean/auto-tag';

/**
 * AICC 시나리오 07: 예약 안내
 *
 * 고객이 서비스 예약을 조회하거나 변경할 때의 ARS 안내 시나리오입니다.
 */
describe('AICC 시나리오 07: 예약 안내', () => {
  it('예약 확인 멘트를 올바르게 변환한다', () => {
    const input = `예약 조회 서비스입니다.

고객님의 예약 내역입니다.

예약번호: 20240115001
예약 일시: 2024-01-20T14:00
서비스: 프리미엄 상담 (60분)
담당자: 김상담

예약 장소: 강남 센터 3층
주소: 서울시 강남구 테헤란로 123
연락처: 02-1234-5678

예약 30분 전 알림 문자가 발송됩니다.

예약 변경은 1번,
예약 취소는 2번,
예약 일정 다시 듣기는 3번,
이전 메뉴로 돌아가시려면 별표를 눌러주세요.`;

    const expected = `예약 조회 서비스입니다.

고객님의 예약 내역입니다.

예약번호: 이 . 공 . 이 . 사 . 공 . 일 . 일 . 오 . 공 . 공 일 예약 일시: 이천이십사년 일 월 이십 일 . 오후 두 시
서비스: 프리미엄 상담 (육십 분)
담당자: 김상담

예약 장소: 강남 센터 삼 층
주소: 서울시 강남구 테헤란로 일 . 이 . 삼
연락처: 공 . 이 . 일 . 이 . 삼 . 사 . 오 . 육 . 칠 . 팔

예약 삼십 분 전 알림 문자가 발송됩니다.

예약 변경은 일 번,
예약 취소는 이 번,
예약 일정 다시 듣기는 삼 번,
이전 메뉴로 돌아가시려면 별표를 눌러주세요.`;

    expect(autoTag(input)).toBe(expected);
  });

  it('예약 변경 완료 멘트를 올바르게 변환한다', () => {
    const input = `예약 변경이 완료되었습니다.

변경 전 일정:
날짜: 2024-01-20
시간: 14:00

변경 후 일정:
날짜: 2024-01-22
시간: 10:30

예약 변경 수수료는 무료입니다.
예약 당일 취소 시 취소 수수료 10,000원이 부과됩니다.

변경된 예약 확인 문자가 010-1234-5678로 발송되었습니다.

확인되셨으면 1번을 눌러주세요.`;

    const expected = `예약 변경이 완료되었습니다.

변경 전 일정:
날짜: 이천이십사년 일 월 이십 일
시간: 오후 두 시

변경 후 일정:
날짜: 이천이십사년 일 월 이십이 일 시간: 오전 열 시 삼십 분

예약 변경 수수료는 무료입니다.
예약 당일 취소 시 취소 수수료 일만 원 이 부과됩니다.

변경된 예약 확인 문자가 공 . 일 . 공 . 일 . 이 . 삼 . 사 . 오 . 육 . 칠 . 팔 로 발송되었습니다.

확인되셨으면 일 번 을 눌러주세요.`;

    expect(autoTag(input)).toBe(expected);
  });

  it('예약 대기 안내 멘트를 올바르게 변환한다', () => {
    const input = `예약 대기 안내입니다.

고객님이 원하시는 시간대는 현재 예약이 마감되었습니다.

희망 일시: 2024-01-20T14:00
현재 대기 순번: 3번째

예상 대기 시간: 약 2h30m
다른 고객의 취소 발생 시 순서대로 예약이 확정됩니다.

대기 접수되면 010-1234-5678로 알림이 발송됩니다.

대기 신청을 원하시면 1번,
다른 시간대 조회는 2번,
이전 메뉴로 돌아가시려면 별표를 눌러주세요.`;

    const expected = `예약 대기 안내입니다.

고객님이 원하시는 시간대는 현재 예약이 마감되었습니다.

희망 일시: 이천이십사년 일 월 이십 일 . 오후 두 시
현재 대기 순번: 세 번째

예상 대기 시간: 약 두 시간 삼십 분
다른 고객의 취소 발생 시 순서대로 예약이 확정됩니다.

대기 접수되면 공 . 일 . 공 . 일 . 이 . 삼 . 사 . 오 . 육 . 칠 . 팔 로 알림이 발송됩니다.

대기 신청을 원하시면 일 번,
다른 시간대 조회는 이 번,
이전 메뉴로 돌아가시려면 별표를 눌러주세요.`;

    expect(autoTag(input)).toBe(expected);
  });
});
