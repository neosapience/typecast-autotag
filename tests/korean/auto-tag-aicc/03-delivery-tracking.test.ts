import { autoTag } from '../../../src/korean/auto-tag';

/**
 * AICC 시나리오 03: 배송 조회
 *
 * 고객이 주문한 상품의 배송 상태를 조회할 때의 ARS 안내 시나리오입니다.
 */
describe('AICC 시나리오 03: 배송 조회', () => {
  it('배송 상태 안내 멘트를 올바르게 변환한다', () => {
    const input = `배송 조회 서비스입니다.

고객님의 최근 주문 내역입니다.

1번째 주문: 주문번호 끝 4자리 5678
상품명: 프리미엄 구독권 3개월
주문일: 2024-01-10
배송 상태: 배송 완료
배송 완료일: 2024-01-12T14:30

2번째 주문: 주문번호 끝 4자리 9012
상품명: 캐릭터 보이스팩 5개
주문일: 2024-01-14
배송 상태: 배송 중
예상 도착일: 2024-01-16

해당 주문 상세 조회는 1번,
다른 주문 조회는 2번,
배송지 변경은 3번,
이전 메뉴로 돌아가시려면 별표를 눌러주세요.`;

    const expected = `배송 조회 서비스입니다.

고객님의 최근 주문 내역입니다.

첫 번째 주문: 주문번호 끝 네자리 오육칠팔
상품명: 프리미엄 구독권 삼 개월
주문일: 이천이십사년 일 월 십 일
배송 상태: 배송 완료
배송 완료일: 이천이십사년 일 월 십이 일 오후 두 시 삼십 분

두 번째 주문: 주문번호 끝 네자리 구영일이
상품명: 캐릭터 보이스팩 다섯 개
주문일: 이천이십사년 일 월 십사 일
배송 상태: 배송 중
예상 도착일: 이천이십사년 일 월 십육 일

해당 주문 상세 조회는 일 번,
다른 주문 조회는 이 번,
배송지 변경은 삼 번,
이전 메뉴로 돌아가시려면 별표를 눌러주세요.`;

    expect(autoTag(input)).toBe(expected);
  });

  it('배송 지연 안내 멘트를 올바르게 변환한다', () => {
    const input = `고객님, 불편을 드려 죄송합니다.

주문번호 끝 4자리 3456 상품의 배송이 지연되고 있습니다.

원래 예상 도착일: 2024-01-15
변경된 예상 도착일: 2024-01-18

지연 사유: 물류센터 물량 증가로 인한 출고 지연
예상 지연 시간: 약 72시간

보상으로 5000원 할인 쿠폰이 발급되었습니다.
쿠폰 유효기간: 2024-02-15까지

배송 취소를 원하시면 1번,
상담원 연결을 원하시면 0번을 눌러주세요.`;

    const expected = `고객님, 불편을 드려 죄송합니다.

주문번호 끝 네자리 삼사오육 상품의 배송이 지연되고 있습니다.

원래 예상 도착일: 이천이십사년 일 월 십오 일
변경된 예상 도착일: 이천이십사년 일 월 십팔 일

지연 사유: 물류센터 물량 증가로 인한 출고 지연
예상 지연 시간: 약 칠십이 시간

보상으로 오천 원 할인 쿠폰이 발급되었습니다.
쿠폰 유효기간: 이천이십사년 이 월 십오 일 까지

배송 취소를 원하시면 일 번,
상담원 연결을 원하시면 영 번 을 눌러주세요.`;

    expect(autoTag(input)).toBe(expected);
  });

  it('수령 확인 안내 멘트를 올바르게 변환한다', () => {
    const input = `배송 완료 확인 안내입니다.

고객님, 2024-01-15T11:23에 상품이 배송 완료되었습니다.

배송 정보:
수령인: 홍길동
배송지: 서울시 강남구
배송 기사 연락처: 010-1234-5678

상품 수령을 확인하셨으면 1번,
미수령이면 2번,
파손/불량 신고는 3번을 눌러주세요.

7일 이내 교환/환불 신청이 가능합니다.`;

    const expected = `배송 완료 확인 안내입니다.

고객님, 이천이십사년 일 월 십오 일 오전 열한 시 이십삼 분 에 상품이 배송 완료되었습니다.

배송 정보:
수령인: 홍길동
배송지: 서울시 강남구
배송 기사 연락처: 공 일 공 다시 일 이 삼 사 다시 오 육 칠 팔

상품 수령을 확인하셨으면 일 번,
미수령이면 이 번,
파손/불량 신고는 삼 번 을 눌러주세요.

칠일 이내 교환/환불 신청이 가능합니다.`;

    expect(autoTag(input)).toBe(expected);
  });
});
