import { date } from '../../../src/korean/tags/date';

describe('date', () => {
  describe('기본 동작', () => {
    it('생년월일을 한글로 변환한다', () => {
      expect(date(19940616)).toBe('천구백구십사년 육 월 십육 일생');
    });

    it('2000년대 생년월일도 처리한다', () => {
      expect(date(20001225)).toBe('이천년 십이 월 이십오 일생');
    });

    it('문자열 입력도 처리한다', () => {
      expect(date('19940616')).toBe('천구백구십사년 육 월 십육 일생');
    });
  });

  describe('다양한 날짜', () => {
    it('1월 1일을 처리한다', () => {
      expect(date('20000101')).toBe('이천년 일 월 일 일생');
    });

    it('12월 31일을 처리한다', () => {
      expect(date('19991231')).toBe('천구백구십구년 십이 월 삼십일 일생');
    });
  });

  describe('구분자가 있는 날짜', () => {
    it('하이픈(-) 구분자를 처리한다', () => {
      expect(date('1994-06-16')).toBe('천구백구십사년 육 월 십육 일생');
      expect(date('2000-12-25')).toBe('이천년 십이 월 이십오 일생');
    });

    it('슬래시(/) 구분자를 처리한다', () => {
      expect(date('1994/06/16')).toBe('천구백구십사년 육 월 십육 일생');
      expect(date('2000/12/25')).toBe('이천년 십이 월 이십오 일생');
    });

    it('점(.) 구분자를 처리한다', () => {
      expect(date('1994.06.16')).toBe('천구백구십사년 육 월 십육 일생');
      expect(date('2000.12.25')).toBe('이천년 십이 월 이십오 일생');
    });

    it('구분자가 있는 1월 1일을 처리한다', () => {
      expect(date('2000-01-01')).toBe('이천년 일 월 일 일생');
    });

    it('구분자가 있는 12월 31일을 처리한다', () => {
      expect(date('1999-12-31')).toBe('천구백구십구년 십이 월 삼십일 일생');
    });
  });

  describe('한글 년월일 형식', () => {
    it('한글 년월일을 처리한다 (붙여쓰기)', () => {
      expect(date('1994년6월16일')).toBe('천구백구십사년 육 월 십육 일생');
    });

    it('한글 년월일을 처리한다 (띄어쓰기)', () => {
      expect(date('1994년 6월 16일')).toBe('천구백구십사년 육 월 십육 일생');
    });

    it('한글 년월일생을 처리한다', () => {
      expect(date('1994년6월16일생')).toBe('천구백구십사년 육 월 십육 일생');
    });

    it('한글 년만 있는 경우를 처리한다', () => {
      expect(date('1994년')).toBe('천구백구십사년생');
    });

    it('한글 년생을 처리한다', () => {
      expect(date('1994년생')).toBe('천구백구십사년생');
    });

    it('한글 월일만 있는 경우를 처리한다', () => {
      expect(date('6월16일')).toBe('육 월 십육 일생');
    });

    it('한글 월일을 띄어쓰기로 처리한다', () => {
      expect(date('6월 16일')).toBe('육 월 십육 일생');
    });
  });

  describe('영어 년월일 형식', () => {
    it('영어 월 일, 년 형식을 처리한다', () => {
      expect(date('June 16, 1994')).toBe('천구백구십사년 육 월 십육 일생');
    });

    it('영어 월 일 년 형식 (쉼표 없이)을 처리한다', () => {
      expect(date('June 16 1994')).toBe('천구백구십사년 육 월 십육 일생');
    });

    it('영어 일 월 년 형식을 처리한다', () => {
      expect(date('16 June 1994')).toBe('천구백구십사년 육 월 십육 일생');
    });

    it('영어 월 약어를 처리한다', () => {
      expect(date('Jun 16, 1994')).toBe('천구백구십사년 육 월 십육 일생');
      expect(date('Dec 25, 2000')).toBe('이천년 십이 월 이십오 일생');
    });

    it('영어 월 년 형식을 처리한다', () => {
      expect(date('June 1994')).toBe('천구백구십사년 육 월');
    });

    it('다양한 영어 월을 처리한다', () => {
      expect(date('January 1, 2000')).toBe('이천년 일 월 일 일생');
      expect(date('February 14, 2000')).toBe('이천년 이 월 십사 일생');
      expect(date('March 3, 2000')).toBe('이천년 삼 월 삼 일생');
      expect(date('April 4, 2000')).toBe('이천년 사 월 사 일생');
      expect(date('May 5, 2000')).toBe('이천년 오 월 오 일생');
      expect(date('July 7, 2000')).toBe('이천년 칠 월 칠 일생');
      expect(date('August 8, 2000')).toBe('이천년 팔 월 팔 일생');
      expect(date('September 9, 2000')).toBe('이천년 구 월 구 일생');
      expect(date('October 10, 2000')).toBe('이천년 십 월 십 일생');
      expect(date('November 11, 2000')).toBe('이천년 십일 월 십일 일생');
      expect(date('December 12, 2000')).toBe('이천년 십이 월 십이 일생');
    });
  });

  describe('미국식 날짜 형식 (MM-DD-YYYY)', () => {
    it('미국식 하이픈 구분자를 처리한다', () => {
      expect(date('06-16-1994')).toBe('천구백구십사년 육 월 십육 일생');
    });

    it('미국식 슬래시 구분자를 처리한다', () => {
      expect(date('12/25/2000')).toBe('이천년 십이 월 이십오 일생');
    });

    it('미국식 점 구분자를 처리한다', () => {
      expect(date('01.01.2000')).toBe('이천년 일 월 일 일생');
    });
  });

  describe('잘못된 날짜 형식 (발음 테스트)', () => {
    it('0월 0일도 발음한다', () => {
      expect(date('2000년 00월 00일')).toBe('이천년 영 월 영 일생');
    });

    it('0월도 발음한다', () => {
      expect(date('2000년 0월 1일')).toBe('이천년 영 월 일 일생');
    });

    it('0일도 발음한다', () => {
      expect(date('2000년 1월 0일')).toBe('이천년 일 월 영 일생');
    });

    it('한 자리 월일도 발음한다', () => {
      expect(date('2000-1-1')).toBe('이천년 일 월 일 일생');
    });
  });

  describe('엣지 케이스', () => {
    it('8자리가 아닌 입력은 그대로 반환한다', () => {
      expect(date('1994061')).toBe('1994061');
      expect(date('199406161')).toBe('199406161');
    });

    it('숫자가 아닌 입력은 그대로 반환한다', () => {
      expect(date('abcdefgh')).toBe('abcdefgh');
    });

    it('인식할 수 없는 형식은 그대로 반환한다', () => {
      expect(date('hello world')).toBe('hello world');
    });
  });
});
