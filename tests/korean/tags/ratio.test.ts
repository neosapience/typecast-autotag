import { ratio } from '../../../src/korean/tags/ratio';

describe('ratio', () => {
  describe('콜론 비율 - 기본', () => {
    it('1:1을 일대일로 변환한다', () => {
      expect(ratio('1:1')).toBe('일대일');
    });

    it('7:3을 칠대삼으로 변환한다', () => {
      expect(ratio('7:3')).toBe('칠대삼');
    });

    it('1:2:3을 일대이대삼으로 변환한다', () => {
      expect(ratio('1:2:3')).toBe('일대이대삼');
    });

    it('10:90을 십대구십으로 변환한다', () => {
      expect(ratio('10:90')).toBe('십대구십');
    });

    it('50:50을 오십대오십으로 변환한다', () => {
      expect(ratio('50:50')).toBe('오십대오십');
    });
  });

  describe('콜론 비율 - 공백 포함', () => {
    it('1 : 1을 일대일로 변환한다', () => {
      expect(ratio('1 : 1')).toBe('일대일');
    });

    it('7 :3을 칠대삼으로 변환한다', () => {
      expect(ratio('7 :3')).toBe('칠대삼');
    });

    it('1: 2: 3을 일대이대삼으로 변환한다', () => {
      expect(ratio('1: 2: 3')).toBe('일대이대삼');
    });
  });

  describe('퍼센트', () => {
    it('15%를 십오 퍼센트로 변환한다', () => {
      expect(ratio('15%')).toBe('십오 퍼센트');
    });

    it('100%를 백 퍼센트로 변환한다', () => {
      expect(ratio('100%')).toBe('백 퍼센트');
    });

    it('0%를 영 퍼센트로 변환한다', () => {
      expect(ratio('0%')).toBe('영 퍼센트');
    });

    it('50%를 오십 퍼센트로 변환한다', () => {
      expect(ratio('50%')).toBe('오십 퍼센트');
    });

    it('1%를 일 퍼센트로 변환한다', () => {
      expect(ratio('1%')).toBe('일 퍼센트');
    });
  });

  describe('퍼센트 - 소수점', () => {
    it('3.5%를 삼쩜오 퍼센트로 변환한다', () => {
      expect(ratio('3.5%')).toBe('삼쩜오 퍼센트');
    });

    it('0.1%를 영쩜일 퍼센트로 변환한다', () => {
      expect(ratio('0.1%')).toBe('영쩜일 퍼센트');
    });

    it('99.99%를 구십구쩜구구 퍼센트로 변환한다', () => {
      expect(ratio('99.99%')).toBe('구십구쩜구구 퍼센트');
    });
  });

  describe('퍼센트 - 공백 포함', () => {
    it('15 %를 십오 퍼센트로 변환한다', () => {
      expect(ratio('15 %')).toBe('십오 퍼센트');
    });
  });

  describe('배수 (고유어 사용)', () => {
    it('2배를 두 배로 변환한다', () => {
      expect(ratio('2배')).toBe('두 배');
    });

    it('10배를 열 배로 변환한다', () => {
      expect(ratio('10배')).toBe('열 배');
    });

    it('100배를 백 배로 변환한다 (100 이상은 한자어)', () => {
      expect(ratio('100배')).toBe('백 배');
    });

    it('1배를 한 배로 변환한다', () => {
      expect(ratio('1배')).toBe('한 배');
    });

    it('3배를 세 배로 변환한다', () => {
      expect(ratio('3배')).toBe('세 배');
    });

    it('99배를 아흔아홉 배로 변환한다', () => {
      expect(ratio('99배')).toBe('아흔아홉 배');
    });
  });

  describe('배수 - 소수점', () => {
    it('1.5배를 한쩜오 배로 변환한다', () => {
      expect(ratio('1.5배')).toBe('한쩜오 배');
    });

    it('2.5배를 두쩜오 배로 변환한다', () => {
      expect(ratio('2.5배')).toBe('두쩜오 배');
    });

    it('0.5배를 영쩜오 배로 변환한다', () => {
      expect(ratio('0.5배')).toBe('영쩜오 배');
    });
  });

  describe('배수 - 공백 포함', () => {
    it('2 배를 두 배로 변환한다', () => {
      expect(ratio('2 배')).toBe('두 배');
    });
  });

  describe('숫자만 입력', () => {
    it('숫자만 입력하면 퍼센트로 변환한다', () => {
      expect(ratio(50)).toBe('오십 퍼센트');
    });

    it('문자열 숫자만 입력해도 퍼센트로 변환한다', () => {
      expect(ratio('123')).toBe('백이십삼 퍼센트');
    });

    it('0만 입력하면 영 퍼센트로 변환한다', () => {
      expect(ratio(0)).toBe('영 퍼센트');
    });

    it('음수도 퍼센트로 변환한다', () => {
      expect(ratio(-5)).toBe('마이너스 오 퍼센트');
    });

    it('소수점 숫자도 퍼센트로 변환한다', () => {
      expect(ratio('3.5')).toBe('삼쩜오 퍼센트');
    });
  });

  describe('엣지 케이스', () => {
    it('빈 문자열은 그대로 반환한다', () => {
      expect(ratio('')).toBe('');
    });

    it('일반 텍스트는 원본 반환한다', () => {
      expect(ratio('hello')).toBe('hello');
    });
  });

  describe('옵션 - 구분자', () => {
    it('구분자를 변경할 수 있다', () => {
      expect(ratio('1:2', { separator: ' 대 ' })).toBe('일 대 이');
    });
  });

  describe('옵션 - 퍼센트 단위', () => {
    it('퍼센트 단위를 변경할 수 있다', () => {
      expect(ratio('50%', { percentUnit: '프로' })).toBe('오십 프로');
    });
  });
});
