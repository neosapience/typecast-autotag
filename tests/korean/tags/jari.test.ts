import { jari } from '../../../src/korean/tags/jari';

describe('jari', () => {
  describe('자리 수 - 기본', () => {
    it('1자리를 한자리로 변환한다', () => {
      expect(jari('1자리')).toBe('한자리');
    });

    it('2자리를 두자리로 변환한다', () => {
      expect(jari('2자리')).toBe('두자리');
    });

    it('3자리를 세자리로 변환한다', () => {
      expect(jari('3자리')).toBe('세자리');
    });

    it('4자리를 네자리로 변환한다', () => {
      expect(jari('4자리')).toBe('네자리');
    });

    it('5자리를 다섯자리로 변환한다', () => {
      expect(jari('5자리')).toBe('다섯자리');
    });

    it('6자리를 여섯자리로 변환한다', () => {
      expect(jari('6자리')).toBe('여섯자리');
    });

    it('7자리를 일곱자리로 변환한다', () => {
      expect(jari('7자리')).toBe('일곱자리');
    });

    it('8자리를 여덟자리로 변환한다', () => {
      expect(jari('8자리')).toBe('여덟자리');
    });

    it('9자리를 아홉자리로 변환한다', () => {
      expect(jari('9자리')).toBe('아홉자리');
    });

    it('10자리를 열자리로 변환한다', () => {
      expect(jari('10자리')).toBe('열자리');
    });
  });

  describe('자리 수 - 10 이상', () => {
    it('11자리를 열한자리로 변환한다', () => {
      expect(jari('11자리')).toBe('열한자리');
    });

    it('12자리를 열두자리로 변환한다', () => {
      expect(jari('12자리')).toBe('열두자리');
    });

    it('13자리를 열세자리로 변환한다', () => {
      expect(jari('13자리')).toBe('열세자리');
    });

    it('20자리를 스물자리로 변환한다', () => {
      expect(jari('20자리')).toBe('스물자리');
    });

    it('21자리를 스물한자리로 변환한다', () => {
      expect(jari('21자리')).toBe('스물한자리');
    });
  });

  describe('자리 수 + 후행 숫자', () => {
    it('4자리 5678을 네자리 오 . 육 . 칠 . 팔로 변환한다', () => {
      expect(jari('4자리 5678')).toBe('네자리 오 . 육 . 칠 . 팔');
    });

    it('4자리 1234를 네자리 일 . 이 . 삼 . 사로 변환한다', () => {
      expect(jari('4자리 1234')).toBe('네자리 일 . 이 . 삼 . 사');
    });

    it('6자리 123456을 여섯자리 일 . 이 . 삼 . 사 . 오 . 육으로 변환한다', () => {
      expect(jari('6자리 123456')).toBe('여섯자리 일 . 이 . 삼 . 사 . 오 . 육');
    });

    it('후행 숫자에 0이 포함된 경우 영으로 변환한다', () => {
      expect(jari('4자리 1023')).toBe('네자리 일 . 영 . 이 . 삼');
    });

    it('후행 숫자가 0000인 경우 영 . 영 . 영 . 영으로 변환한다', () => {
      expect(jari('4자리 0000')).toBe('네자리 영 . 영 . 영 . 영');
    });
  });

  describe('0자리', () => {
    it('0자리를 영자리로 변환한다', () => {
      expect(jari('0자리')).toBe('영자리');
    });
  });

  describe('접두사 포함', () => {
    it('끝 4자리 5678을 끝 네자리 오 . 육 . 칠 . 팔로 변환한다', () => {
      expect(jari('끝 4자리 5678')).toBe('끝 네자리 오 . 육 . 칠 . 팔');
    });

    it('마지막 2자리를 마지막 두자리로 변환한다', () => {
      expect(jari('마지막 2자리')).toBe('마지막 두자리');
    });
  });

  describe('엣지 케이스', () => {
    it('빈 문자열은 그대로 반환한다', () => {
      expect(jari('')).toBe('');
    });

    it('자리가 없는 문자열은 원본 반환한다', () => {
      expect(jari('hello')).toBe('hello');
    });

    it('숫자만 있는 경우 원본 반환한다', () => {
      expect(jari('1234')).toBe('1234');
    });
  });

  describe('옵션 - 자리 앞 공백', () => {
    it('자리 앞에 공백을 추가할 수 있다', () => {
      expect(jari('4자리', { includeSpaceBeforeJari: true })).toBe('네 자리');
    });
  });

  describe('옵션 - 후행 숫자 구분자', () => {
    it('후행 숫자 사이에 구분자를 추가할 수 있다', () => {
      expect(jari('4자리 1234', { digitSeparator: ' ' })).toBe('네자리 일 이 삼 사');
    });
  });
});
