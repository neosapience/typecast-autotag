import { datetime } from '../../../src/korean/tags/datetime';

describe('datetime', () => {
  describe('ISO 8601 형식', () => {
    it('2024-01-15T14:30을 변환한다', () => {
      expect(datetime('2024-01-15T14:30')).toBe('이천이십사년 일 월 십오 일 오후 두 시 삼십 분');
    });

    it('2024-06-10T09:05를 변환한다 (유월)', () => {
      expect(datetime('2024-06-10T09:05')).toBe('이천이십사년 유 월 십 일 오전 아홉 시 오 분');
    });

    it('2024-10-01T12:00을 변환한다 (시월)', () => {
      expect(datetime('2024-10-01T12:00')).toBe('이천이십사년 시 월 일 일 오후 열두 시');
    });

    it('초 단위가 포함된 경우도 변환한다', () => {
      expect(datetime('2024-01-15T14:30:45')).toBe(
        '이천이십사년 일 월 십오 일 오후 두 시 삼십 분 사십오 초'
      );
    });
  });

  describe('공백 구분 형식', () => {
    it('2024-01-15 14:30을 변환한다', () => {
      expect(datetime('2024-01-15 14:30')).toBe('이천이십사년 일 월 십오 일 오후 두 시 삼십 분');
    });

    it('2024/06/10 09:05:30을 변환한다', () => {
      expect(datetime('2024/06/10 09:05:30')).toBe(
        '이천이십사년 유 월 십 일 오전 아홉 시 오 분 삼십 초'
      );
    });

    it('2024.12.25 18:00을 변환한다', () => {
      expect(datetime('2024.12.25 18:00')).toBe('이천이십사년 십이 월 이십오 일 오후 여섯 시');
    });
  });

  describe('한글 형식', () => {
    it('2024년 1월 15일 14시 30분을 변환한다', () => {
      expect(datetime('2024년 1월 15일 14시 30분')).toBe(
        '이천이십사년 일 월 십오 일 오후 두 시 삼십 분'
      );
    });

    it('2024년 6월 10일 9시를 변환한다', () => {
      expect(datetime('2024년 6월 10일 9시')).toBe('이천이십사년 유 월 십 일 오전 아홉 시');
    });

    it('오전/오후가 포함된 한글 형식도 변환한다', () => {
      expect(datetime('2024년 1월 15일 오후 2시 30분')).toBe(
        '이천이십사년 일 월 십오 일 오후 두 시 삼십 분'
      );
    });
  });

  describe('숫자 형식', () => {
    it('YYYYMMDD HHMM 형식을 변환한다', () => {
      expect(datetime('20240115 1430')).toBe('이천이십사년 일 월 십오 일 오후 두 시 삼십 분');
    });

    it('YYYYMMDD HHMMSS 형식을 변환한다', () => {
      expect(datetime('20240115 143045')).toBe(
        '이천이십사년 일 월 십오 일 오후 두 시 삼십 분 사십오 초'
      );
    });
  });

  describe('24시간제 옵션', () => {
    it('24시간제로 변환한다', () => {
      expect(datetime('2024-01-15T14:30', { use24Hour: true })).toBe(
        '이천이십사년 일 월 십오 일 열네 시 삼십 분'
      );
    });

    it('24시간제에서 23시도 변환한다', () => {
      expect(datetime('2024-01-15T23:00', { use24Hour: true })).toBe(
        '이천이십사년 일 월 십오 일 스물세 시'
      );
    });
  });

  describe('엣지 케이스', () => {
    it('빈 문자열은 그대로 반환한다', () => {
      expect(datetime('')).toBe('');
    });

    it('공백만 있는 문자열은 그대로 반환한다', () => {
      expect(datetime('   ')).toBe('   ');
    });

    it('잘못된 형식은 원본 반환한다', () => {
      expect(datetime('abc')).toBe('abc');
    });

    it('날짜만 있는 경우 원본 반환한다', () => {
      expect(datetime('2024-01-15')).toBe('2024-01-15');
    });

    it('시간만 있는 경우 원본 반환한다', () => {
      expect(datetime('14:30')).toBe('14:30');
    });
  });

  describe('생년월일 표기 제거 확인', () => {
    it('datetime에서는 "일생"이 아닌 "일"로 끝난다', () => {
      const result = datetime('2024-01-15T14:30');
      expect(result).not.toContain('일생');
      expect(result).toContain('일 오후');
    });
  });

  describe('separator 옵션', () => {
    it('기본 separator는 공백이다', () => {
      const result = datetime('2024-01-15T14:30');
      expect(result).toBe('이천이십사년 일 월 십오 일 오후 두 시 삼십 분');
    });

    it('custom separator를 적용한다', () => {
      expect(datetime('2024-01-15T14:30', { separator: ', ' })).toBe(
        '이천이십사년 일 월 십오 일, 오후 두 시 삼십 분'
      );
    });

    it('빈 문자열 separator를 적용한다', () => {
      expect(datetime('2024-01-15T14:30', { separator: '' })).toBe(
        '이천이십사년 일 월 십오 일오후 두 시 삼십 분'
      );
    });
  });

  describe('자정과 정오', () => {
    it('자정(00:00)을 변환한다', () => {
      expect(datetime('2024-01-15T00:00')).toBe('이천이십사년 일 월 십오 일 오전 열두 시');
    });

    it('정오(12:00)를 변환한다', () => {
      expect(datetime('2024-01-15T12:00')).toBe('이천이십사년 일 월 십오 일 오후 열두 시');
    });

    it('자정을 24시간제로 변환한다', () => {
      expect(datetime('2024-01-15T00:00', { use24Hour: true })).toBe(
        '이천이십사년 일 월 십오 일 영 시'
      );
    });
  });

  describe('경계값 테스트', () => {
    it('23:59:59를 변환한다', () => {
      expect(datetime('2024-12-31T23:59:59')).toBe(
        '이천이십사년 십이 월 삼십일 일 오후 열한 시 오십구 분 오십구 초'
      );
    });

    it('초가 0인 경우 초를 표시하지 않는다', () => {
      const result = datetime('2024-01-15T14:30:00');
      expect(result).not.toContain('초');
      expect(result).toBe('이천이십사년 일 월 십오 일 오후 두 시 삼십 분');
    });

    it('분과 초가 모두 0인 경우', () => {
      expect(datetime('2024-01-15T14:00:00')).toBe('이천이십사년 일 월 십오 일 오후 두 시');
    });
  });

  describe('유효하지 않은 입력', () => {
    it('유효하지 않은 시간(25시)은 원본 반환한다', () => {
      expect(datetime('2024-01-15T25:00')).toBe('2024-01-15T25:00');
    });

    it('유효하지 않은 분(60분)은 원본 반환한다', () => {
      expect(datetime('2024-01-15T14:60')).toBe('2024-01-15T14:60');
    });

    it('유효하지 않은 초(60초)는 원본 반환한다', () => {
      expect(datetime('2024-01-15T14:30:60')).toBe('2024-01-15T14:30:60');
    });
  });

  describe('ISO 8601 확장 형식', () => {
    it('밀리초가 포함된 경우도 변환한다', () => {
      expect(datetime('2024-01-15T14:30:45.123')).toBe(
        '이천이십사년 일 월 십오 일 오후 두 시 삼십 분 사십오 초'
      );
    });

    it('타임존 Z가 포함된 경우도 변환한다', () => {
      expect(datetime('2024-01-15T14:30:00Z')).toBe(
        '이천이십사년 일 월 십오 일 오후 두 시 삼십 분'
      );
    });

    it('타임존 +09:00이 포함된 경우도 변환한다', () => {
      expect(datetime('2024-01-15T14:30:00+09:00')).toBe(
        '이천이십사년 일 월 십오 일 오후 두 시 삼십 분'
      );
    });

    it('타임존 -05:00이 포함된 경우도 변환한다', () => {
      expect(datetime('2024-06-10T09:05:30-05:00')).toBe(
        '이천이십사년 유 월 십 일 오전 아홉 시 오 분 삼십 초'
      );
    });

    it('밀리초와 타임존이 함께 포함된 경우도 변환한다', () => {
      expect(datetime('2024-01-15T14:30:45.123Z')).toBe(
        '이천이십사년 일 월 십오 일 오후 두 시 삼십 분 사십오 초'
      );
    });
  });

  describe('한글 형식 초 단위', () => {
    it('한글 형식에서 초가 포함된 경우도 변환한다', () => {
      expect(datetime('2024년 1월 15일 14시 30분 45초')).toBe(
        '이천이십사년 일 월 십오 일 오후 두 시 삼십 분 사십오 초'
      );
    });

    it('오전/오후와 초가 포함된 한글 형식도 변환한다', () => {
      expect(datetime('2024년 1월 15일 오후 2시 30분 45초')).toBe(
        '이천이십사년 일 월 십오 일 오후 두 시 삼십 분 사십오 초'
      );
    });
  });
});
