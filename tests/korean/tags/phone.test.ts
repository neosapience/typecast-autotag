import { phone } from '../../../src/korean/tags/phone';

describe('phone', () => {
  describe('기본 동작', () => {
    it('휴대폰 번호를 발음 교정 형태로 변환한다', () => {
      expect(phone('010-2055-4783')).toBe('공일공 다시 이공오오 다시 사칠팔삼');
    });

    it('서울 지역번호도 처리한다', () => {
      expect(phone('02-1234-5678')).toBe('공이 다시 일이삼사 다시 오육칠팔');
    });

    it('일반 지역번호도 처리한다', () => {
      expect(phone('031-123-4567')).toBe('공삼일 다시 일이삼 다시 사오육칠');
    });
  });

  describe('커스텀 구분자', () => {
    it('커스텀 구분자를 사용할 수 있다', () => {
      expect(phone('010-1234-5678', ' ')).toBe('공일공 일이삼사 오육칠팔');
    });
  });

  describe('엣지 케이스', () => {
    it('빈 문자열은 그대로 반환한다', () => {
      expect(phone('')).toBe('');
    });

    it('하이픈 없는 번호도 처리한다', () => {
      expect(phone('01012345678')).toBe('공일공일이삼사오육칠팔');
    });
  });
});
