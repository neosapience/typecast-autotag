import { money } from '../../../src/korean/tags/money';

describe('money', () => {
  describe('기본 동작 - 원화', () => {
    it('1000을 천 원으로 변환한다', () => {
      expect(money(1000)).toBe('천 원');
    });

    it('10000을 일만 원으로 변환한다', () => {
      expect(money(10000)).toBe('일만 원');
    });

    it('15000을 일만오천 원으로 변환한다', () => {
      expect(money(15000)).toBe('일만오천 원');
    });

    it('50000을 오만 원으로 변환한다', () => {
      expect(money(50000)).toBe('오만 원');
    });

    it('100000을 십만 원으로 변환한다', () => {
      expect(money(100000)).toBe('십만 원');
    });

    it('1000000을 백만 원으로 변환한다', () => {
      expect(money(1000000)).toBe('백만 원');
    });

    it('10000000을 천만 원으로 변환한다', () => {
      expect(money(10000000)).toBe('천만 원');
    });

    it('100000000을 일억 원으로 변환한다', () => {
      expect(money(100000000)).toBe('일억 원');
    });
  });

  describe('다양한 금액', () => {
    it('500을 오백 원으로 변환한다', () => {
      expect(money(500)).toBe('오백 원');
    });

    it('3500을 삼천오백 원으로 변환한다', () => {
      expect(money(3500)).toBe('삼천오백 원');
    });

    it('12345를 일만이천삼백사십오 원으로 변환한다', () => {
      expect(money(12345)).toBe('일만이천삼백사십오 원');
    });

    it('987654321을 구억팔천칠백육십오만사천삼백이십일 원으로 변환한다', () => {
      expect(money(987654321)).toBe('구억팔천칠백육십오만사천삼백이십일 원');
    });

    it('0이 포함된 숫자 10001을 일만일 원으로 변환한다', () => {
      expect(money(10001)).toBe('일만일 원');
    });

    it('0이 포함된 숫자 100100을 십만백 원으로 변환한다', () => {
      expect(money(100100)).toBe('십만백 원');
    });
  });

  describe('큰 숫자 (조 단위)', () => {
    it('1조를 일조 원으로 변환한다', () => {
      expect(money(1000000000000)).toBe('일조 원');
    });

    it('10조를 십조 원으로 변환한다', () => {
      expect(money(10000000000000)).toBe('십조 원');
    });

    it('복잡한 조 단위 숫자를 변환한다', () => {
      expect(money(1234567890123)).toBe('일조이천삼백사십오억육천칠백팔십구만백이십삼 원');
    });
  });

  describe('엣지 케이스', () => {
    it('0을 영 원으로 변환한다', () => {
      expect(money(0)).toBe('영 원');
    });

    it('음수는 원본 반환한다', () => {
      expect(money(-1000)).toBe('-1000');
    });

    it('빈 문자열은 그대로 반환한다', () => {
      expect(money('')).toBe('');
    });

    it('NaN은 원본 반환한다', () => {
      expect(money(NaN)).toBe('NaN');
    });

    it('Infinity는 원본 반환한다', () => {
      expect(money(Infinity)).toBe('Infinity');
    });

    it('-Infinity는 원본 반환한다', () => {
      expect(money(-Infinity)).toBe('-Infinity');
    });

    it('숫자가 없는 문자열은 원본 반환한다', () => {
      expect(money('abc')).toBe('abc');
    });

    it('숫자 앞에 텍스트가 있으면 원본 반환한다', () => {
      expect(money('약 5000원')).toBe('약 5000원');
    });
  });

  describe('문자열 입력', () => {
    it('"10000원"을 일만 원으로 변환한다', () => {
      expect(money('10000원')).toBe('일만 원');
    });

    it('"10,000원"을 일만 원으로 변환한다 (천단위 구분자)', () => {
      expect(money('10,000원')).toBe('일만 원');
    });

    it('"5,500"을 오천오백 원으로 변환한다', () => {
      expect(money('5,500')).toBe('오천오백 원');
    });

    it('"₩10000"을 일만 원으로 변환한다', () => {
      expect(money('₩10000')).toBe('일만 원');
    });

    it('"$100"을 백 원으로 변환한다 (기호만 제거)', () => {
      expect(money('$100')).toBe('백 원');
    });

    it('공백이 있는 문자열도 처리한다', () => {
      expect(money('  5000원  ')).toBe('오천 원');
    });

    it('"0원"을 영 원으로 변환한다', () => {
      expect(money('0원')).toBe('영 원');
    });

    it('"0"을 영 원으로 변환한다', () => {
      expect(money('0')).toBe('영 원');
    });

    it('"€100"을 백 원으로 변환한다 (유로 기호 제거)', () => {
      expect(money('€100')).toBe('백 원');
    });

    it('"£100"을 백 원으로 변환한다 (파운드 기호 제거)', () => {
      expect(money('£100')).toBe('백 원');
    });

    it('"¥100"을 백 원으로 변환한다 (엔 기호 제거)', () => {
      expect(money('¥100')).toBe('백 원');
    });
  });

  describe('옵션 - 단위', () => {
    it('단위를 달러로 설정할 수 있다', () => {
      expect(money(100, { unit: '달러' })).toBe('백 달러');
    });

    it('단위를 엔으로 설정할 수 있다', () => {
      expect(money(1000, { unit: '엔' })).toBe('천 엔');
    });

    it('단위를 유로로 설정할 수 있다', () => {
      expect(money(50, { unit: '유로' })).toBe('오십 유로');
    });
  });

  describe('옵션 - 공백', () => {
    it('공백을 제거할 수 있다', () => {
      expect(money(10000, { includeSpace: false })).toBe('일만원');
    });

    it('단위와 공백 옵션을 함께 사용할 수 있다', () => {
      expect(money(100, { unit: '달러', includeSpace: false })).toBe('백달러');
    });
  });

  describe('한글 큰 단위', () => {
    it('"1억원"을 일 억원으로 변환한다', () => {
      expect(money('1억원')).toBe('일 억원');
    });

    it('"1000만원"을 천 만원으로 변환한다', () => {
      expect(money('1000만원')).toBe('천 만원');
    });

    it('"100만원"을 백 만원으로 변환한다', () => {
      expect(money('100만원')).toBe('백 만원');
    });

    it('"10만원"을 십 만원으로 변환한다', () => {
      expect(money('10만원')).toBe('십 만원');
    });

    it('"5천원"을 오 천원으로 변환한다', () => {
      expect(money('5천원')).toBe('오 천원');
    });

    it('"1조원"을 일 조원으로 변환한다', () => {
      expect(money('1조원')).toBe('일 조원');
    });

    it('"1경원"을 일 경원으로 변환한다', () => {
      expect(money('1경원')).toBe('일 경원');
    });

    it('"50억원"을 오십 억원으로 변환한다', () => {
      expect(money('50억원')).toBe('오십 억원');
    });

    it('"천단위 구분자가 있는 1,000만원"을 천 만원으로 변환한다', () => {
      expect(money('1,000만원')).toBe('천 만원');
    });

    it('"0만원"을 영 만원으로 변환한다', () => {
      expect(money('0만원')).toBe('영 만원');
    });
  });
});
