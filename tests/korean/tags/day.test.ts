import { day } from '../../../src/korean/tags/day';

describe('day', () => {
  describe('기본 동작', () => {
    it('25를 이십오일로 변환한다', () => {
      expect(day(25)).toBe('이십오일');
    });

    it('1을 일일로 변환한다', () => {
      expect(day(1)).toBe('일일');
    });

    it('15를 십오일로 변환한다', () => {
      expect(day(15)).toBe('십오일');
    });

    it('31을 삼십일일로 변환한다', () => {
      expect(day(31)).toBe('삼십일일');
    });

    it('10을 십일로 변환한다', () => {
      expect(day(10)).toBe('십일');
    });
  });

  describe('문자열 입력 (숫자만)', () => {
    it('문자열 숫자도 처리한다', () => {
      expect(day('25')).toBe('이십오일');
    });

    it('문자열 "1"을 일일로 변환한다', () => {
      expect(day('1')).toBe('일일');
    });
  });

  describe('"일" 접미사가 포함된 입력', () => {
    it('"25일"을 이십오일로 변환한다', () => {
      expect(day('25일')).toBe('이십오일');
    });

    it('"1일"을 일일로 변환한다', () => {
      expect(day('1일')).toBe('일일');
    });

    it('"100일"을 백일로 변환한다', () => {
      expect(day('100일')).toBe('백일');
    });
  });

  describe('기간/일수 표현 (31일 초과)', () => {
    it('365일을 삼백육십오일로 변환한다', () => {
      expect(day(365)).toBe('삼백육십오일');
    });

    it('100일을 백일로 변환한다', () => {
      expect(day(100)).toBe('백일');
    });

    it('1000일을 천일로 변환한다', () => {
      expect(day(1000)).toBe('천일');
    });

    it('"365일" 문자열을 삼백육십오일로 변환한다', () => {
      expect(day('365일')).toBe('삼백육십오일');
    });
  });

  describe('0일 처리', () => {
    it('0을 영일로 변환한다', () => {
      expect(day(0)).toBe('영일');
    });

    it('"0일"을 영일로 변환한다', () => {
      expect(day('0일')).toBe('영일');
    });
  });

  describe('D-day 형식', () => {
    it('"D-3418일"을 디데이 삼천사백십팔일로 변환한다', () => {
      expect(day('D-3418일')).toBe('디데이 삼천사백십팔일');
    });

    it('"D-1일"을 디데이 일일로 변환한다', () => {
      expect(day('D-1일')).toBe('디데이 일일');
    });

    it('"D-100일"을 디데이 백일로 변환한다', () => {
      expect(day('D-100일')).toBe('디데이 백일');
    });

    it('"D+100일"을 디데이 플러스 백일로 변환한다', () => {
      expect(day('D+100일')).toBe('디데이 플러스 백일');
    });

    it('"D-0일"을 디데이로 변환한다', () => {
      expect(day('D-0일')).toBe('디데이');
    });

    it('"D+0일"을 디데이로 변환한다', () => {
      expect(day('D+0일')).toBe('디데이');
    });

    it('소문자 "d-100일"도 처리한다', () => {
      expect(day('d-100일')).toBe('디데이 백일');
    });

    it('"일" 없이 "D-100"도 처리한다', () => {
      expect(day('D-100')).toBe('디데이 백일');
    });

    it('공백이 있는 "D - 100일"도 처리한다', () => {
      expect(day('D - 100일')).toBe('디데이 백일');
    });

    it('공백이 있는 "D + 50일"도 처리한다', () => {
      expect(day('D + 50일')).toBe('디데이 플러스 오십일');
    });

    it('"D-Day" 숫자 없이도 디데이로 변환한다', () => {
      expect(day('D-Day')).toBe('디데이');
    });

    it('"D-day" 소문자도 디데이로 변환한다', () => {
      expect(day('D-day')).toBe('디데이');
    });

    it('"d-day" 전체 소문자도 디데이로 변환한다', () => {
      expect(day('d-day')).toBe('디데이');
    });
  });

  describe('천단위 구분자', () => {
    it('"1,000일"을 천일로 변환한다', () => {
      expect(day('1,000일')).toBe('천일');
    });

    it('"10,000일"을 일만일로 변환한다', () => {
      expect(day('10,000일')).toBe('일만일');
    });

    it('"1,234,567일"을 변환한다', () => {
      expect(day('1,234,567일')).toBe('백이십삼만사천오백육십칠일');
    });

    it('"1,000" (일 없이)도 처리한다', () => {
      expect(day('1,000')).toBe('천일');
    });
  });

  describe('앞자리 0 (leading zeros)', () => {
    it('"01일"을 일일로 변환한다', () => {
      expect(day('01일')).toBe('일일');
    });

    it('"007일"을 칠일로 변환한다', () => {
      expect(day('007일')).toBe('칠일');
    });

    it('"01"을 일일로 변환한다', () => {
      expect(day('01')).toBe('일일');
    });

    it('"00"을 영일로 변환한다', () => {
      expect(day('00')).toBe('영일');
    });
  });

  describe('N일째/N일차 형식', () => {
    it('"100일째"를 백일째로 변환한다', () => {
      expect(day('100일째')).toBe('백일째');
    });

    it('"365일차"를 삼백육십오일차로 변환한다', () => {
      expect(day('365일차')).toBe('삼백육십오일차');
    });

    it('"1일째"를 일일째로 변환한다', () => {
      expect(day('1일째')).toBe('일일째');
    });

    it('"1,000일째"를 천일째로 변환한다', () => {
      expect(day('1,000일째')).toBe('천일째');
    });

    it('"D-100일째"는 처리하지 않고 그대로 반환한다', () => {
      // D-day와 일째의 조합은 의미가 맞지 않으므로 그대로 반환
      expect(day('D-100일째')).toBe('D-100일째');
    });
  });

  describe('엣지 케이스', () => {
    it('음수는 그대로 반환한다', () => {
      expect(day(-1)).toBe('-1');
    });

    it('유효하지 않은 문자열은 그대로 반환한다', () => {
      expect(day('abc')).toBe('abc');
    });

    it('빈 문자열은 그대로 반환한다', () => {
      expect(day('')).toBe('');
    });

    it('혼합된 유효하지 않은 문자열은 그대로 반환한다', () => {
      expect(day('abc123일')).toBe('abc123일');
    });
  });
});
