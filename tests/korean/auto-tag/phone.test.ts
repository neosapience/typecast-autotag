import { autoTag, autoPhone } from '../../../src/korean/auto-tag';

describe('autoTag - phone (전화번호 자동 태깅)', () => {
  describe('휴대폰 번호', () => {
    it('010-XXXX-XXXX 형식을 변환한다', () => {
      expect(autoPhone('전화번호는 010-1234-5678입니다.')).toBe(
        '전화번호는 공 일 공 다시 일 이 삼 사 다시 오 육 칠 팔입니다.'
      );
    });

    it('010.XXXX.XXXX 형식을 변환한다', () => {
      expect(autoPhone('연락처: 010.9876.5432')).toBe(
        '연락처: 공 일 공 다시 구 팔 칠 육 다시 오 사 삼 이'
      );
    });

    it('공백으로 구분된 전화번호를 변환한다', () => {
      expect(autoPhone('010 1234 5678로 연락주세요')).toBe(
        '공 일 공 다시 일 이 삼 사 다시 오 육 칠 팔로 연락주세요'
      );
    });

    it('구분자 없는 전화번호를 변환한다', () => {
      // 구분자 없는 11자리 숫자도 전화번호로 인식 (phone 함수는 구분자 없이 처리)
      expect(autoPhone('01012345678')).toBe('공 일 공 일 이 삼 사 오 육 칠 팔');
    });

    it('011, 016, 017, 018, 019 번호를 변환한다', () => {
      expect(autoPhone('011-123-4567')).toBe('공 일 일 다시 일 이 삼 다시 사 오 육 칠');
      expect(autoPhone('016-234-5678')).toBe('공 일 육 다시 이 삼 사 다시 오 육 칠 팔');
    });
  });

  describe('지역번호', () => {
    it('서울 지역번호(02)를 변환한다', () => {
      expect(autoPhone('02-1234-5678')).toBe('공 이 다시 일 이 삼 사 다시 오 육 칠 팔');
      expect(autoPhone('02-123-4567')).toBe('공 이 다시 일 이 삼 다시 사 오 육 칠');
    });

    it('경기 지역번호(031)를 변환한다', () => {
      expect(autoPhone('031-234-5678')).toBe('공 삼 일 다시 이 삼 사 다시 오 육 칠 팔');
    });

    it('부산 지역번호(051)를 변환한다', () => {
      expect(autoPhone('051-456-7890')).toBe('공 오 일 다시 사 오 육 다시 칠 팔 구 공');
    });
  });

  describe('대표번호', () => {
    it('1588 대표번호를 변환한다', () => {
      expect(autoPhone('1588-1234')).toBe('일 오 팔 팔 다시 일 이 삼 사');
    });

    it('1544 대표번호를 변환한다', () => {
      expect(autoPhone('고객센터: 1544-9999')).toBe('고객센터: 일 오 사 사 다시 구 구 구 구');
    });

    it('1600, 1666, 1800 대표번호를 변환한다', () => {
      expect(autoPhone('1600-1234')).toBe('일 육 공 공 다시 일 이 삼 사');
      expect(autoPhone('1666-5678')).toBe('일 육 육 육 다시 오 육 칠 팔');
      expect(autoPhone('1800-9012')).toBe('일 팔 공 공 다시 구 공 일 이');
    });
  });

  describe('긴급/특수번호', () => {
    it('112, 119를 변환한다', () => {
      expect(autoPhone('긴급시 112로 신고하세요')).toBe('긴급시 일 일 이로 신고하세요');
      expect(autoPhone('화재시 119')).toBe('화재시 일 일 구');
    });

    it('110을 변환한다', () => {
      expect(autoPhone('정부민원 110')).toBe('정부민원 일 일 공');
    });
  });

  describe('복수 전화번호', () => {
    it('여러 전화번호를 모두 변환한다', () => {
      const result = autoPhone('대표: 1588-1234, 직통: 02-123-4567');
      expect(result).toBe(
        '대표: 일 오 팔 팔 다시 일 이 삼 사, 직통: 공 이 다시 일 이 삼 다시 사 오 육 칠'
      );
    });
  });

  describe('False Positive 방지', () => {
    it('일반 숫자는 변환하지 않는다', () => {
      // 4자리 숫자는 전화번호가 아님
      expect(autoPhone('주문번호 1234')).toBe('주문번호 1234');
    });

    it('년도는 변환하지 않는다', () => {
      expect(autoTag('2024년도 계획', { enabledTags: ['phone'] })).toBe('2024년도 계획');
    });

    it('날짜는 전화번호로 인식하지 않는다', () => {
      expect(autoTag('2024-01-15', { enabledTags: ['phone'] })).toBe('2024-01-15');
    });

    it('시간은 전화번호로 인식하지 않는다', () => {
      expect(autoTag('14:30', { enabledTags: ['phone'] })).toBe('14:30');
    });
  });

  describe('문맥 내 전화번호', () => {
    it('문장 중간의 전화번호를 변환한다', () => {
      expect(autoPhone('김철수(010-1234-5678)님께 연락드렸습니다.')).toBe(
        '김철수(공 일 공 다시 일 이 삼 사 다시 오 육 칠 팔)님께 연락드렸습니다.'
      );
    });

    it('괄호 안 전화번호를 변환한다', () => {
      // (02)1234-5678 형식은 현재 패턴으로 인식 안됨 - 표준 형식 사용 권장
      expect(autoPhone('문의: 02-1234-5678')).toBe('문의: 공 이 다시 일 이 삼 사 다시 오 육 칠 팔');
    });
  });
});
